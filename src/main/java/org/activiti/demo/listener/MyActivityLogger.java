package org.activiti.demo.listener;

import org.activiti.engine.delegate.DelegateExecution;
import org.activiti.engine.delegate.ExecutionListener;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.stereotype.Component;

/**
 * @author Yvo Swillens
 */
@Component("myActivityLogger")
public class MyActivityLogger implements ExecutionListener {

    private static final Logger logger = LoggerFactory.getLogger(MyActivityLogger.class);

    public void notify(DelegateExecution delegateExecution) {
        logger.debug("Current activity id: <{}> ", delegateExecution.getCurrentActivityId());
    }
}
